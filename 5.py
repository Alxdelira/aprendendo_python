a = int(input("Digite um numero\t"))
b = int(input("Digite um numero\t"))

print("A Soma {} + {} = {}".format(a,b,a + b))
print("A Subtração {} - {} = {}".format(a,b,a - b))
print("A Multiplicação {} * {} = {}".format(a,b,a * b))
print("A Divisão {} / {} = {:.2f}".format(a,b,a / b))  #{:.2f} serve para demilitar as casas decimais do valor resultado


